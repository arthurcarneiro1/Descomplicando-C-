// Operadores aritimeticos

int n1= 0;
//+ : adição e concatena~]ao
n1=2+2;
//- : subtração
n1=2-2;
// * : multiplicação
n1 = 2*2; 
// / : divisão
n1 = 2 /2;
// % : resto de divisão
n1= 4%2;

// encremento e decrementeo rapido
//++ : encremento
n1++;
//-- : decremento
n1--;

//OBJ MATH
// possue varias funçoes que ajudam a realizar calculos

//raiz
Math.Sqrt(n1);

//potencia
Math.Pow(n1,2);

//trigonometria
Math.Tan(n1); // tangente
Math.Cos(n1); // cosseno
Math.Sin(n1); //seno